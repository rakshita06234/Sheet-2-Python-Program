day_number = int(input("Enter a number from 1 to 7: "))

if day_number == 1:
    print("sunday")
elif day_number == 2:
    print("monday")
elif day_number == 3:
    print("tuesday")
elif day_number == 4:
    print("wednesday")
elif day_number == 5:
    print("thursday")
elif day_number == 6:
    print("friday")
elif day_number == 7:
    print("saturday")
else:
    print(" Please enter a number from 1 to 7.")